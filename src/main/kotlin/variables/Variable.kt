package variables

// use val or var to declare variables
fun main() {
    // variables and data types
    val name: String = "Kotlin"
    val count: Int = 6
    val letter: Char = 'A'
    var a: Short = 5
    var b: Long = 67
    var d: Double = 56.0
    val alive: Boolean = true

    //output
    println("Name: $name")
    println("Count = $count")
    print("character $letter")
    println("Short $a")
    println("Long $b")
    println("Double $d")
    println("Health alive $alive")
}